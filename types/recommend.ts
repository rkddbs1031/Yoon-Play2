interface List {
  genre: string[];
  playlist: string[];
}

export interface Recommendation {
  description: string;
  list: null | List;
}

export enum RecommendationResultType {
  Playlist = 'playlist',
  Genre = 'genre',
}

export enum RecommendationType {
  Mood = 'mood', // 기분/상태 기반
  Weather = 'weather', // 날씨 기반
  Artist = 'artist', // 좋아하는 가수 기반
  Time = 'time', // 시간대 기반
  Activity = 'activity', // 활동 기반
}

export interface RandomHeadLineType {
  type: RecommendationType | null;
  text: string;
}
